<h2 style="color:#FF0000">Eligibility Requirements</h2><br />
<p>To ensure the safety of blood donation for donors and recipients, all blood donors must be evaluated to determine elegibility.</p>
<h3>Blood Donors Must Be:</h3>
<p><b>Be In Good General Health </b>Healthy means that you feel and can perform normal activities.</p>
<p><b>Be at least 17-yrs in most states </b>or 16-yrs-old with parental consent if allowed by state law</p>
<p><b>Weight at least 100lbs </b>Additional weight requirements apply for donors 18-years-old and younger and all high school donors.</p>
<p>Other aspects of each potential donor's health history are discussed as part of the donation process before any blood is collected. Each donor receives a brief examination during which temperature, pulse, blood pressure and haemoglobin (or haematocrit) are measured.<br />Making donations for your own use during surgery (autologous blood donation) is considere a medical procedure that requires a written prescription and the rules for eligiblity are less strict than for regular volunteer donations.</p>
